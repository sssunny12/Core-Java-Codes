class Doubleplus
{
	public static void main(String args[])
	{
	
		double first_number = Double.parseDouble(args[0]);
		double second_number = Double.parseDouble(args[1]);
		double third_number = Double.parseDouble(args[2]);
		double sum = first_number + second_number + third_number;

	System.out.println("Result = " + sum);
}
}